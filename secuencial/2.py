""""
Construya un algoritmo que calcule el perímetro y el área de un rectángulo dada su base y su altura.
La base y la altura deberán ser almacenadas previamente en dos variables respectivamente. El
algoritmo debe mostrar en pantalla el siguiente mensaje: El perímetro del rectángulo es: xxx el área
del rectángulo es: yyyy
"""
base=15 #aqui asignamos el valor a la variable
altura=7 #aqui asignamos el valor a la variable
area= base*altura #en esta variable multiplicamos las dos variables anteriores para sacar el area
perimetro= (2*base)+(2*altura) #aqui se realiza el perimetro del rectangulo
print(f"El perimetro del rectangulo es: {perimetro}, el area del rectangulo es: {area}") #lo que mostramos en la consola